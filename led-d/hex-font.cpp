    bool font_t::fill (led_d::font_t &font, id_t id)
    {
      switch (id) {
      case font_name_t::id_ibm:
        fill (font, ibm_font, 0);
        break;
      case font_name_t::id_greek:
        fill (font, greek_font, 65);
        break;
      case font_name_t::id_slim:
        fill (font, slim_font, 32);
        break;
      default:
        return false;
        break;
      }

      return true;
    }

    bool font_t::fill (led_d::font_t &font, const hex_font_t &hex_font, std::size_t shift)
    {
      for (std::size_t id = 0; id < hex_font.size (); ++id) {
        row_vector_t row_vector (hex_font[id]);
        matrix_t symbol;
        const std::size_t start_x (symbol.size ());
        for (std::size_t row_y = 0; row_y < row_vector.size (); ++row_y)
          symbol.add_row (row_vector[row_y], row_y, start_x);
        font.add_symbol (static_cast<char>(id + shift), symbol);
      }
      
      return true;
    }

    bool font_t::fill (led_d::font_t &font, const column_vector_t &hex_font, std::size_t shift)
    {
      static const std::size_t column_per_symbol = 5;
      matrix_t symbol;
      
      for (std::size_t column_id = 0; column_id < hex_font.size (); ++ column_id) {
        symbol.add (hex_font[column_id]);
        if (column_id % column_per_symbol == 4) {
          symbol.add (column_t ()); // characters too close, separate them
          char symbol_id = static_cast<char>(column_id / column_per_symbol + shift);
          font.add_symbol (symbol_id, symbol);
          symbol.clear ();
        }
      }
      
      return true;
    }

